public class Main4While {
    public static void main(String[] args) {
        int i = 0;
        while (i<3) {
            System.out.println(i);
            //i = i+1;
            //i+=1;
            i++;

        }
        i=0;
        //fori - сокращение

        for (i=0;i<3;i++){
            System.out.println(i);
        }
        i=0;
        do {
            System.out.println(i);
            i++;}
        while (i<3);



    }
}
